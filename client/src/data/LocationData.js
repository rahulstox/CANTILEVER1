// Helper function to get random location from a category
export const getRandomLocation = (category, destination) => {
  const locations = locationData[destination]?.[category] || genericLocations[category];
  return locations[Math.floor(Math.random() * locations.length)];
};

// Helper function to get multiple random locations
export const getRandomLocations = (category, destination, count = 1) => {
  const locations = locationData[destination]?.[category] || genericLocations[category];
  const shuffled = [...locations].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}; 